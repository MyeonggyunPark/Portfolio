// Ïπ¥ÌÖåÍ≥†Î¶¨ ÏÑ†ÌÉù UI Ï∂úÎ†• / Show category selection UI
function showCategorySelector() {
  fetch("/order/api/menu-categories/")
    .then((res) => res.json())
    .then((data) => {
      const container = document.getElementById("menu-selector");
      container.innerHTML = `<h3 class="font-semibold">Ïπ¥ÌÖåÍ≥†Î¶¨ ÏÑ†ÌÉù / Choose Category</h3>`;
      data.categories.forEach((cat) => {
        container.innerHTML += `
          <button onclick="showMenusByCategory(${cat.id})"
                  class="px-3 py-1 bg-gray-200 rounded hover:bg-gray-300">${cat.name}</button>
        `;
      });
      container.classList.remove("hidden");
    });
}

// Ïπ¥ÌÖåÍ≥†Î¶¨ ÎÇ¥ Î©îÎâ¥ Ï∂úÎ†• / Show menu items within selected category
function showMenusByCategory(categoryId) {
  fetch(`/order/api/menus-by-category/${categoryId}/`)
    .then((res) => res.json())
    .then((data) => {
      const container = document.getElementById("menu-selector");
      container.innerHTML = `<h3 class="font-semibold mb-2">Î©îÎâ¥ ÏÑ†ÌÉù / Choose Menu</h3>`;
      data.menus.forEach((menu) => {
        container.innerHTML += `
          <button onclick="addMenuToOrder(${menu.id})"
                  class="block w-full text-left px-4 py-2 border rounded mb-1 hover:bg-gray-100">
            ${menu.name} - ${menu.price.toFixed(2)} ‚Ç¨
          </button>
        `;
      });
    });
}

